class caca:
    def __init__(self):
        self.x = 0
        self.y = 0
    
    def calc_sum(self):
        return self.x + self.y

    def calc_multiply(self):
        return self.x * self.y

    def calc_substract(self):
        return self.x - self.y

    def calc_divide(self):
        return self.x / self.y

caca = caca()